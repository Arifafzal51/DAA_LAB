public class nestedloop {
    public static void main(String[] args) {

        int n=5;
        for (int i = 0; i < n; i++) {
            for (int j = 1; j < i; j++) {
                for (int k = 1; k <=100 ; k++) {
                    System.out.println("DAA");
                }

            }

        }
    }
}



